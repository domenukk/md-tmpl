// typeKind classifies a parsed type.
type typeKind int

const (
	kindStr typeKind = iota
	kindInt
	kindFloat
	kindBool
	kindList
	kindStruct
	kindEnum
	kindOption     // option(T) — nullable wrapper.
	kindScalarList // scalar_list(T) — homogeneous typed list.
	kindAlias      // Unresolved type alias — maps to any.
)

// typeNode is a parsed type specification.
type typeNode struct {
	kind      typeKind
	fields    []fieldNode   // For list and struct.
	variants  []variantNode // For enum.
	innerType *typeNode     // For option and scalar_list.
}

// fieldNode is a name-type pair inside a list/struct/variant.
type fieldNode struct {
	name     string
	typeNode typeNode
}

// variantNode is one variant of an enum.
type variantNode struct {
	name   string
	fields []fieldNode // Empty for unit variants.
}

// parseTypeSpec parses a TypeSpec string like "str", "list(x = str, y = int)",
// "enum(A, B(x = str))", etc.
func parseTypeSpec(spec string) (typeNode, error) {
	spec = strings.TrimSpace(spec)
	p := &typeParser{input: spec}
	node, err := p.parseType()
	if err != nil {
		return typeNode{}, err
	}
	return node, nil
}

// typeParser is a simple recursive-descent parser for TypeSpec strings.
type typeParser struct {
	input string
	pos   int
}

func (p *typeParser) parseType() (typeNode, error) {
	p.skipWhitespace()
	ident := p.readIdent()
	if ident == "" {
		return typeNode{}, fmt.Errorf("expected type identifier at position %d in %q", p.pos, p.input)
	}

	switch ident {
	case "str":
		return typeNode{kind: kindStr}, nil
	case "int":
		return typeNode{kind: kindInt}, nil
	case "float":
		return typeNode{kind: kindFloat}, nil
	case "bool":
		return typeNode{kind: kindBool}, nil
	case "list":
		return p.parseCompound(kindList)
	case "struct":
		return p.parseCompound(kindStruct)
	case "enum":
		return p.parseEnum()
	case "option":
		return p.parseWrapped(kindOption)
	case "scalar_list":
		return p.parseWrapped(kindScalarList)
	default:
		// Type alias or unknown type — use any. The engine resolves
		// aliases at runtime; codegen cannot expand them without the
		// full type alias map.
		return typeNode{kind: kindAlias}, nil
	}
}

// parseWrapped handles option(T) and scalar_list(T) — types wrapping a single inner type.
func (p *typeParser) parseWrapped(kind typeKind) (typeNode, error) {
	p.skipWhitespace()
	if !p.consume('(') {
		return typeNode{}, fmt.Errorf("expected '(' after type keyword at position %d in %q", p.pos, p.input)
	}
	p.skipWhitespace()

	inner, err := p.parseType()
	if err != nil {
		return typeNode{}, fmt.Errorf("parsing inner type: %w", err)
	}

	p.skipWhitespace()
	if !p.consume(')') {
		return typeNode{}, fmt.Errorf("expected ')' at position %d in %q", p.pos, p.input)
	}

	return typeNode{kind: kind, innerType: &inner}, nil
}

func (p *typeParser) parseCompound(kind typeKind) (typeNode, error) {
	p.skipWhitespace()
	if !p.consume('(') {
		// Bare list/struct without fields.
		return typeNode{kind: kind}, nil
	}

	// For list types, check if this is a scalar list (e.g., list(str))
	// by peeking ahead: if the first token is a type keyword not followed
	// by '=', it's a scalar list.
	if kind == kindList {
		saved := p.pos
		p.skipWhitespace()
		ident := p.readIdent()
		p.skipWhitespace()
		if ident != "" && p.peek() == ')' {
			// It's a scalar list like list(str).
			p.pos++ // consume ')'
			innerNode, err := parseTypeSpec(ident)
			if err != nil {
				return typeNode{}, fmt.Errorf("parsing scalar list element type: %w", err)
			}
			return typeNode{kind: kindScalarList, innerType: &innerNode}, nil
		}
		// Not a scalar list — restore position and parse as normal fields.
		p.pos = saved
	}

	fields, err := p.parseFields(')')
	if err != nil {
		return typeNode{}, err
	}

	return typeNode{kind: kind, fields: fields}, nil
}

func (p *typeParser) parseFields(closer rune) ([]fieldNode, error) {
	var fields []fieldNode
	for {
		p.skipWhitespace()
		if p.peek() == byte(closer) {
			p.pos++
			break
		}
		if len(fields) > 0 {
			if !p.consume(',') {
				return nil, fmt.Errorf("expected ',' or '%c' at position %d in %q", closer, p.pos, p.input)
			}
			p.skipWhitespace()
		}

		name := p.readIdent()
		if name == "" {
			return nil, fmt.Errorf("expected field name at position %d in %q", p.pos, p.input)
		}
		p.skipWhitespace()
		if !p.consume('=') {
			return nil, fmt.Errorf("expected '=' after field name %q at position %d in %q", name, p.pos, p.input)
		}
		p.skipWhitespace()

		fieldType, err := p.parseType()
		if err != nil {
			return nil, fmt.Errorf("parsing type for field %q: %w", name, err)
		}
		fields = append(fields, fieldNode{name: name, typeNode: fieldType})
	}
	return fields, nil
}

func (p *typeParser) parseEnum() (typeNode, error) {
	p.skipWhitespace()
	if !p.consume('(') {
		return typeNode{}, fmt.Errorf("expected '(' after 'enum' at position %d in %q", p.pos, p.input)
	}

	var variants []variantNode
	for {
		p.skipWhitespace()
		if p.peek() == ')' {
			p.pos++
			break
		}
		if len(variants) > 0 {
			if !p.consume(',') {
				return typeNode{}, fmt.Errorf("expected ',' or ')' at position %d in %q", p.pos, p.input)
			}
			p.skipWhitespace()
		}

		name := p.readIdent()
		if name == "" {
			return typeNode{}, fmt.Errorf("expected variant name at position %d in %q", p.pos, p.input)
		}

		p.skipWhitespace()
		var fields []fieldNode
		if p.peek() == '(' {
			p.pos++ // consume '('
			var err error
			fields, err = p.parseFields(')')
			if err != nil {
				return typeNode{}, fmt.Errorf("parsing variant %q fields: %w", name, err)
			}
		}

		variants = append(variants, variantNode{name: name, fields: fields})
	}

	return typeNode{kind: kindEnum, variants: variants}, nil
}

func (p *typeParser) skipWhitespace() {
	for p.pos < len(p.input) && (p.input[p.pos] == ' ' || p.input[p.pos] == '\t' || p.input[p.pos] == '\n' || p.input[p.pos] == '\r') {
		p.pos++
	}
}

func (p *typeParser) readIdent() string {
	start := p.pos
	for p.pos < len(p.input) {
		ch := p.input[p.pos]
		if ch == '_' || (ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z') || (ch >= '0' && ch <= '9' && p.pos > start) {
			p.pos++
		} else {
			break
		}
	}
	return p.input[start:p.pos]
}

func (p *typeParser) peek() byte {
	if p.pos >= len(p.input) {
		return 0
	}
	return p.input[p.pos]
}

func (p *typeParser) consume(ch byte) bool {
	p.skipWhitespace()
	if p.pos < len(p.input) && p.input[p.pos] == ch {
		p.pos++
		return true
	}
	return false
}

