// Code generation core
// ---------------------------------------------------------------------------

// codegenContext accumulates generated Go source pieces.
type codegenContext struct {
	opts           *genOptions
	auxiliaryTypes []string        // Nested struct/enum definitions.
	generatedNames map[string]bool // Dedup set for generated type names.
}

func newCodegenContext(opts *genOptions) *codegenContext {
	return &codegenContext{
		opts:           opts,
		generatedNames: make(map[string]bool),
	}
}

func generateFromTemplate(tmpl *Template, opts *genOptions) (string, error) {
	decls := tmpl.Declarations()

	ctx := newCodegenContext(opts)

	// First pass: resolve all types (may generate auxiliary definitions).
	type fieldInfo struct {
		name     string
		goType   string
		typeKind typeKind // Retained for generating optimal setter calls.
	}
	fields := make([]fieldInfo, 0, len(decls))
	for _, d := range decls {
		parsed, err := parseTypeSpec(d.Type)
		if err != nil {
			return "", fmt.Errorf("parsing type for %q: %w", d.Name, err)
		}
		goType := ctx.resolveType(d.Name, parsed)
		fields = append(fields, fieldInfo{name: d.Name, goType: goType, typeKind: parsed.kind})
	}

	// Build the output.
	var b strings.Builder

	b.WriteString("// Code generated by pt-gen-go; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", opts.packageName)

	// Imports — include prompt_templates for the Render helper.
	if opts.renderHelper {
		b.WriteString("import (\n")
		b.WriteString("\t\"github.com/domenukk/prompt-templates/go/prompt_templates\"\n")
		b.WriteString(")\n\n")
	}

	// Auxiliary types (enums, nested structs).
	for _, aux := range ctx.auxiliaryTypes {
		b.WriteString(aux)
		b.WriteString("\n\n")
	}

	// Main params struct.
	fmt.Fprintf(&b, "// %s holds the typed parameters for template rendering.\n", opts.paramsName)
	fmt.Fprintf(&b, "type %s struct {\n", opts.paramsName)
	for _, f := range fields {
		fmt.Fprintf(&b, "\t// %s parameter.\n", toPascalCase(f.name))
		fmt.Fprintf(&b, "\t%s %s `json:\"%s\"`\n", toPascalCase(f.name), f.goType, f.name)
	}
	b.WriteString("}\n")

	// Render helper method — emit direct SetStr/SetInt/etc. calls for scalar
	// fields to bypass reflect + FlexBuffers serialization entirely.
	if opts.renderHelper {
		b.WriteString("\n")
		fmt.Fprintf(&b, "// Render renders the template with these typed parameters.\n")
		fmt.Fprintf(&b, "//\n")
		fmt.Fprintf(&b, "// Scalar fields use direct FFI setters (no reflection or serialization).\n")
		fmt.Fprintf(&b, "// Complex fields (list, struct, enum) are set via FlexBuffers encoding.\n")
		fmt.Fprintf(&b, "func (p %s) Render(tmpl *prompt_templates.Template) (string, error) {\n", opts.paramsName)
		b.WriteString("\tctx := prompt_templates.NewContext()\n")
		b.WriteString("\tdefer ctx.Close()\n")
		for _, f := range fields {
			goField := toPascalCase(f.name)
			switch f.typeKind {
			case kindStr:
				fmt.Fprintf(&b, "\tif err := ctx.SetStr(%q, string(p.%s)); err != nil {\n\t\treturn \"\", err\n\t}\n", f.name, goField)
			case kindInt:
				fmt.Fprintf(&b, "\tif err := ctx.SetInt(%q, int64(p.%s)); err != nil {\n\t\treturn \"\", err\n\t}\n", f.name, goField)
			case kindFloat:
				fmt.Fprintf(&b, "\tif err := ctx.SetFloat(%q, float64(p.%s)); err != nil {\n\t\treturn \"\", err\n\t}\n", f.name, goField)
			case kindBool:
				fmt.Fprintf(&b, "\tif err := ctx.SetBool(%q, bool(p.%s)); err != nil {\n\t\treturn \"\", err\n\t}\n", f.name, goField)
			default:
				// Complex types (list, struct, enum): use generic Set which
				// marshals via FlexBuffers — still a single FFI call.
				fmt.Fprintf(&b, "\tif err := ctx.Set(%q, p.%s); err != nil {\n\t\treturn \"\", err\n\t}\n", f.name, goField)
			}
		}
		b.WriteString("\treturn tmpl.Render(ctx)\n")
		b.WriteString("}\n")
	}

	// Format with gofmt.
	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		return b.String(), fmt.Errorf("formatting generated code: %w\n\nRaw output:\n%s", err, b.String())
	}
	return string(formatted), nil
}

// resolveType converts a parsed typeNode into a Go type string, emitting
// auxiliary type definitions as needed.
func (c *codegenContext) resolveType(fieldName string, node typeNode) string {
	switch node.kind {
	case kindStr:
		return "string"
	case kindInt:
		return "int64"
	case kindFloat:
		return "float64"
	case kindBool:
		return "bool"

	case kindList:
		if len(node.fields) == 0 {
			return "[]any"
		}
		itemName := toPascalCase(fieldName) + "Item"
		c.emitStruct(itemName, node.fields)
		return "[]" + itemName

	case kindStruct:
		if len(node.fields) == 0 {
			return "map[string]any"
		}
		structName := toPascalCase(fieldName)
		c.emitStruct(structName, node.fields)
		return structName

	case kindEnum:
		enumName := toPascalCase(fieldName)
		c.emitEnum(enumName, node.variants)
		return enumName

	case kindOption:
		if node.innerType == nil {
			return "any"
		}
		inner := c.resolveType(fieldName, *node.innerType)
		return "*" + inner

	case kindScalarList:
		if node.innerType == nil {
			return "[]any"
		}
		inner := c.resolveType(fieldName, *node.innerType)
		return "[]" + inner

	case kindAlias:
		return "any"

	default:
		return "any"
	}
}

// emitStruct generates a Go struct type for list items or struct values.
func (c *codegenContext) emitStruct(name string, fields []fieldNode) {
	if c.generatedNames[name] {
		return
	}
	c.generatedNames[name] = true

	var b strings.Builder
	fmt.Fprintf(&b, "// %s is a nested type for structured fields.\n", name)
	fmt.Fprintf(&b, "type %s struct {\n", name)
	for _, f := range fields {
		goType := c.resolveType(f.name, f.typeNode)
		fmt.Fprintf(&b, "\t%s %s `json:\"%s\"`\n", toPascalCase(f.name), goType, f.name)
	}
	b.WriteString("}")

	c.auxiliaryTypes = append(c.auxiliaryTypes, b.String())
}

// emitEnum generates Go type definitions for an enum.
func (c *codegenContext) emitEnum(name string, variants []variantNode) {
	if c.generatedNames[name] {
		return
	}
	c.generatedNames[name] = true

	// Check if all variants are unit (no fields).
	allUnit := true
	for _, v := range variants {
		if len(v.fields) > 0 {
			allUnit = false
			break
		}
	}

	if allUnit {
		c.emitUnitEnum(name, variants)
	} else {
		c.emitMixedEnum(name, variants)
	}
}

// emitUnitEnum generates a string-based enum with const block.
func (c *codegenContext) emitUnitEnum(name string, variants []variantNode) {
	var b strings.Builder
	fmt.Fprintf(&b, "// %s is a string enum type.\n", name)
	fmt.Fprintf(&b, "type %s string\n\n", name)
	fmt.Fprintf(&b, "const (\n")
	for _, v := range variants {
		constName := name + v.name
		fmt.Fprintf(&b, "\t// %s is the %q variant.\n", constName, v.name)
		fmt.Fprintf(&b, "\t%s %s = %q\n", constName, name, v.name)
	}
	b.WriteString(")")

	c.auxiliaryTypes = append(c.auxiliaryTypes, b.String())
}

// emitMixedEnum generates an interface-based sum type for enums with struct variants.
func (c *codegenContext) emitMixedEnum(name string, variants []variantNode) {
	var b strings.Builder

	// Interface type.
	sealMethod := "is" + name
	fmt.Fprintf(&b, "// %s is a sum type (sealed interface).\n", name)
	fmt.Fprintf(&b, "//\n")
	fmt.Fprintf(&b, "// Possible variants:\n")
	for _, v := range variants {
		fmt.Fprintf(&b, "//   - %s%s\n", name, v.name)
	}
	fmt.Fprintf(&b, "type %s interface {\n", name)
	fmt.Fprintf(&b, "\t%s()\n", sealMethod)
	b.WriteString("}\n\n")

	// Variant types.
	for _, v := range variants {
		variantTypeName := name + v.name
		if len(v.fields) == 0 {
			// Unit variant → empty struct.
			fmt.Fprintf(&b, "// %s is the %q unit variant of %s.\n", variantTypeName, v.name, name)
			fmt.Fprintf(&b, "type %s struct{}\n\n", variantTypeName)
		} else {
			// Struct variant.
			fmt.Fprintf(&b, "// %s is the %q struct variant of %s.\n", variantTypeName, v.name, name)
			fmt.Fprintf(&b, "type %s struct {\n", variantTypeName)
			for _, f := range v.fields {
				goType := c.resolveType(f.name, f.typeNode)
				fmt.Fprintf(&b, "\t%s %s `json:\"%s\"`\n", toPascalCase(f.name), goType, f.name)
			}
			b.WriteString("}\n\n")
		}
		fmt.Fprintf(&b, "func (%s) %s() {}\n\n", variantTypeName, sealMethod)
	}

	// Trim trailing newlines.
	result := strings.TrimRight(b.String(), "\n")
	c.auxiliaryTypes = append(c.auxiliaryTypes, result)
}

