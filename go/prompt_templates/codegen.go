// GenOption configures code generation via the functional options pattern.
type GenOption func(*genOptions)

type genOptions struct {
	packageName  string
	paramsName   string
	renderHelper bool
}

func defaultGenOptions() *genOptions {
	return &genOptions{
		packageName:  "main",
		paramsName:   "",
		renderHelper: true,
	}
}

// WithPackageName sets the Go package name in the generated source.
// Default: "main".
func WithPackageName(name string) GenOption {
	return func(o *genOptions) { o.packageName = name }
}

// WithParamsName sets the name of the generated params struct.
// Default: derived from the file stem in PascalCase + "Params".
// When generating from source (no filename), defaults to "Params".
func WithParamsName(name string) GenOption {
	return func(o *genOptions) { o.paramsName = name }
}

// WithRenderHelper controls whether a Render helper method is generated.
// Default: true.
func WithRenderHelper(include bool) GenOption {
	return func(o *genOptions) { o.renderHelper = include }
}

// GenerateTypes generates Go source code from a template source string.
//
// The source must include YAML frontmatter with parameter declarations.
// Returns valid, gofmt-formatted Go source code as a string.
func GenerateTypes(source string, opts ...GenOption) (string, error) {
	o := defaultGenOptions()
	for _, fn := range opts {
		fn(o)
	}
	if o.paramsName == "" {
		o.paramsName = "Params"
	}

	tmpl, err := FromSourceAllowingUnused(source)
	if err != nil {
		return "", fmt.Errorf("parsing template source: %w", err)
	}
	defer tmpl.Close()

	return generateFromTemplate(tmpl, o)
}

// GenerateTypesFromFile generates Go source code from a .tmpl.md file.
//
// The params struct name defaults to PascalCase(fileStem) + "Params"
// unless overridden with [WithParamsName].
func GenerateTypesFromFile(path string, opts ...GenOption) (string, error) {
	o := defaultGenOptions()
	for _, fn := range opts {
		fn(o)
	}
	if o.paramsName == "" {
		stem := strings.TrimSuffix(filepath.Base(path), ".tmpl.md")
		if stem == filepath.Base(path) {
			stem = strings.TrimSuffix(stem, filepath.Ext(stem))
		}
		o.paramsName = toPascalCase(stem) + "Params"
	}

	source, err := os.ReadFile(path)
	if err != nil {
		return "", fmt.Errorf("reading template file: %w", err)
	}

	tmpl, err := FromSourceAllowingUnused(string(source))
	if err != nil {
		return "", fmt.Errorf("parsing template file: %w", err)
	}
	defer tmpl.Close()

	return generateFromTemplate(tmpl, o)
}

